SSD1306::SSD1306(uint8_t devAddr) : _devAddr(devAddr), _i2c(devAddr)
{}

void SSD1306::begin(uint8_t vccState)
{
  _vccState = vccState;

  // Init sequence
  ssd1306command(SSD1306_DISPLAYOFF);                    // 0xAE
  ssd1306command(SSD1306_SETDISPLAYCLOCKDIV);            // 0xD5
  ssd1306command(0x80);                                  // the suggested ratio 0x80

  ssd1306command(SSD1306_SETMULTIPLEX);                  // 0xA8
  ssd1306command(SSD1306_LCDHEIGHT - 1);

  ssd1306command(SSD1306_SETDISPLAYOFFSET);              // 0xD3
  ssd1306command(0x0);                                   // no offset
  ssd1306command(SSD1306_SETSTARTLINE | 0x0);            // line #0
  ssd1306command(SSD1306_CHARGEPUMP);                    // 0x8D
  if (vccState == SSD1306_EXTERNALVCC)
    ssd1306command(0x10);
  else
    ssd1306command(0x14);
  ssd1306command(SSD1306_MEMORYMODE);                    // 0x20
  ssd1306command(0x00);                                  // 0x0 act like ks0108
  ssd1306command(SSD1306_SEGREMAP | 0x1);
  ssd1306command(SSD1306_COMSCANDEC);

  ssd1306command(SSD1306_SETCOMPINS);                    // 0xDA
  ssd1306command(0x12);
  ssd1306command(SSD1306_SETCONTRAST);                   // 0x81
  if (vccState == SSD1306_EXTERNALVCC)
    ssd1306command(0x9F);
  else
    ssd1306command(0xCF);

  ssd1306command(SSD1306_SETPRECHARGE);                  // 0xd9
  if (vccState == SSD1306_EXTERNALVCC)
    ssd1306command(0x22);
  else
    ssd1306command(0xF1);

  ssd1306command(SSD1306_SETVCOMDETECT);                 // 0xDB
  ssd1306command(0x40);
  ssd1306command(SSD1306_DISPLAYALLON_RESUME);           // 0xA4
  ssd1306command(SSD1306_NORMALDISPLAY);                 // 0xA6
  ssd1306command(SSD1306_DEACTIVATE_SCROLL);

  ssd1306command(SSD1306_DISPLAYON);                     //--turn on oled panel
}

void SSD1306::end()
{
  ssd1306command(SSD1306_DISPLAYOFF);
}

void SSD1306::ssd1306command(uint8_t cmd)
{
  // I2Cdev::writeByte(_devAddr, 0x00, cmd);
  _i2c.writeByte(0x00, cmd);
}

void SSD1306::drawPixel(int16_t x, int16_t y, uint16_t color)
{
  if ((x < 0) || (x >= SSD1306_LCDWIDTH) || (y < 0) || (y >= SSD1306_LCDHEIGHT))
    return;

  x = SSD1306_LCDWIDTH - x - 1;
  y = SSD1306_LCDHEIGHT - y - 1;

  switch (color)
  {
    case WHITE:   buffer[x+ (y/8)*SSD1306_LCDWIDTH] |=  (1 << (y&7)); break;
    case BLACK:   buffer[x+ (y/8)*SSD1306_LCDWIDTH] &= ~(1 << (y&7)); break;
    case INVERSE: buffer[x+ (y/8)*SSD1306_LCDWIDTH] ^=  (1 << (y&7)); break;
  }
}

void SSD1306::display()
{
  ssd1306command(SSD1306_COLUMNADDR);
  ssd1306command(0);   // Column start address (0 = reset)
  ssd1306command(SSD1306_LCDWIDTH-1); // Column end address (127 = reset)

  ssd1306command(SSD1306_PAGEADDR);
  ssd1306command(0); // Page start address (0 = reset)
  ssd1306command(7); // Page end address

  for (uint16_t i=0; i<(SSD1306_LCDWIDTH*SSD1306_LCDHEIGHT/8); i++) {
    for (uint8_t x=0; x<16; x++) {
      // I2Cdev::writeByte(_devAddr, 0x40, buffer[i]);
      _i2c.writeByte(0x40, buffer[i]);
      i++;
    }
    i--;
  }
}

void SSD1306::clearDisplay()
{
  memset(buffer, 0, (SSD1306_LCDWIDTH*SSD1306_LCDHEIGHT/8));
}
